var valorUm = 10;
var valorDois = 3;

/*Operador de Atribuição*/
var totalAdicao = valorUm + valorDois;
document.write("A soma de " + valorUm + " + " + valorDois + " é  " + totalAdicao + "<br>" );

/*Operador de Subtração*/
var totalSubtracao = valorUm - valorDois;
document.write("A Subtracao de " + valorUm + " - " + valorDois + " é  " + totalSubtracao + "<br>"  );

/*Operador de Multiplicação*/
var totalMultiplicacao = valorUm * valorDois;
document.write("A Multiplicacao de " + valorUm + " * " + valorDois + " é  " + totalMultiplicacao + "<br>"  );

/*Operador de Divisão*/
var totalDivisao = valorUm / valorDois;
document.write("A Divisao de " + valorUm + " / " + valorDois + " é  " + totalDivisao.toFixed(2) + "<br>"  );

/*Operador de Resto*/
var totalResto = valorUm % valorDois;
document.write("O Resto de " + valorUm + " % " + valorDois + " é  " + totalResto + "<br>"  );