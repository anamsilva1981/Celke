var valorUm = 8;
var valorDois = 3;

valorUm += valorDois;
document.write("Operador de atribuição com soma: " + valorUm + "<br>");

valorUm -= valorDois;
document.write("Operador de atribuição com subtração: " + valorUm + "<br>");

valorUm *= valorDois;
document.write("Operador de atribuição com multiplicação: " + valorUm + "<br>");

valorUm /= valorDois;
document.write("Operador de atribuição com divisão: " + valorUm + "<br>");

valorUm %= valorDois;
document.write("Operador de atribuição com módulo: " + valorUm + "<br>");

valorUm = valorDois;
document.write("Operador de atribuição com aribuição simples: " + valorUm + "<br>");